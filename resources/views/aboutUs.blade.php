Le 6 décembre 2008, le Président des négociations sur l'agriculture, l'Ambassadeur Crawford Falconer, a distribué son dernier projet révisé de “modalités” — une sorte de schéma pour l'accord final. Ce texte était fondé sur des consultations menées depuis septembre, après les négociations du “paquet de juillet 2008”. Les Ministres étaient alors venus à Genève — du 21 au 30 juillet — pour tenter de convenir de “modalités” concernant l'agriculture et l'accès aux marchés pour les produits non agricoles. Même si les réunions de juillet ont abouti à une impasse sur certaines questions, il y a eu rapprochement des vues sur plusieurs autres.

Le projet de “modalités” contient des formules pour abaisser les droits de douane et les subventions ayant des effets de distorsion des échanges, ainsi que des dispositions connexes. Des versions précédentes ont été distribuées le 10 juillet, 19 mai et 8 février 2008.

Ces textes constituaient eux-mêmes des révisions établies à partir d'une version distribuée en juillet-août 2007 et des 16 documents de travail du Président distribués par la suite. En juillet 2008, les changements étaient le fruit de quelque 240 heures de négociations organisées par le Président depuis septembre 2007, soit la phase la plus intensive et productive du Cycle de Doha depuis son lancement en 2001 et depuis le début des négociations sur l'agriculture en mars 2000. Les délégations ont aussi longuement négocié entre elles.<br>
