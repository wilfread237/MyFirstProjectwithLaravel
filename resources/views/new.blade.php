		<div class="field field-name-body field-type-text-with-summary field-label-hidden"><div class="field-items"><div class="field-item even"><p><strong>Le Président de la République,</strong></p>
<p>			Vu</p>
<p>			la Constitution;</p>
<p>			Vu</p>
<p>			le décret n° 2004/320 du 8 décembre 2004 portant organisation du Gouvernement,</p>
<p> </p>
<p><strong>Décrète :</strong></p>
<p> </p>
<p><strong>TITRE I</strong></p>
<p><strong>DISPOSITIONS GENERALES</strong></p>
<p> </p>
<p><strong>Article 1er</strong> :</p>
<p>(1) Le Ministère de l'Agriculture et du Développement Rural est placé sous l'autorité d'un Ministre, assisté d'un Secrétaire d'Etat.</p>
<p>(2) Le Ministre de l'Agriculture et du Développement Rural est chargé de l'élaboration, de la mise en œuvre et de l'évaluation de la politique du Gouvernement dans les domaines de l'agriculture et du développement rural.</p>
<p>A ce titre, il est responsable :</p>
<p><strong>a) en matière agricole :</strong></p>
<p>-          de l'élaboration, de la planification et de la réalisation des programmes gouvernementaux relatifs à l'agriculture et au développement rural;</p>
<p>-          de l'élaboration de la réglementation et des normes, ainsi que du contrôle de leur application;</p>
<p>-          du suivi et de la protection des différentes filières agricoles;</p>
<p>-          de la protection phytosanitaire des végétaux;</p>
<p>-          de la conception des stratégies et des modalités pour garantir  la sécurité et l'autosuffisance alimentaires ainsi que du suivi de leur mise en œuvre;</p>
<p>-          de l'identification et de la promotion de nouvelles productions agricoles pour l'exportation;</p>
<p>-          de la collecte, de la production et de l'analyse des statistiques agricoles;</p>
<p>-          de la diffusion de l'information et des conseils agricoles auprès des producteurs;</p>
<p>-          de la coordination de la gestion des situations de crise en matière agricole ;</p>
<p>-          du suivi des organisations professionnelles agricoles ;</p>
<p>-          de la promotion des investissements, des moyennes et grandes exploitations dans le secteur agricole;</p>
<p>-          de l'enseignement agricole et coopératif et du contrôle de l'enseignement agricole et coopératif et du contrôle de l'enseignement agricole privé, en liaison avec le Ministère chargé de la formation professionnelle.</p>
<p><strong>b) en matière de développement rural :</strong></p>
<p>-          de l'encadrement des paysans et de la vulgarisation agricole;</p>
<p>-          de la participation à la planification des programmes d'amélioration du cadre de vie en milieu rural, en liaison avec les Ministères compétents;</p>
<p>-          du suivi de la réalisation des programmes d'amélioration du cadre de vie en milieu rural;</p>
<p>-          de la promotion du développement communautaire;</p>
<p>-          du génie rural.</p>
<p>(3) Il assure la tutelle des structures de développement en milieu rural ainsi que celle de la «Cameroon Development Corporation », de la Société de Développement du Cacao et de la Chambre d'Agriculture, de l'Elevage et des Forêts.</p>
<p>Il assure la liaison avec l'Organisation des Nations-Unies pour l'Alimentation et l'Agriculture et le Programme Alimentaire Mondial, notamment.</p>
<p> </p>
<p><strong>Article 2</strong> :</p>
<p>(1) Pour l'accomplissement de ses missions, le Ministre de l'Agriculture et du Développement Rural dispose:</p>
<p>-          d'un Secrétariat Particulier;</p>
<p>-          de deux (02) Conseillers Techniques;</p>
<p>-          d'une Inspection Générale;</p>
<p>-          d'une Administration Centrale;</p>
<p>-          de Services Déconcentrés;</p>
<p>-          de Services Rattachés.</p>
<p>(2) Le Secrétaire d'Etat dispose d'un Secrétariat Particulier.</p>
<p> </p>
<p><strong>TITRE Il</strong></p>
<p><strong>DES SECRETARIATS PARTICULIERS</strong></p>
<p> </p>
<p><strong>Article 3</strong> : Placés chacun sous l'autorité d'un Chef de Secrétariat Particulier, les Secrétariats Particuliers sont chargés des affaires réservées du Ministre et du Secrétaire d'Etat.</p>
<p> </p>
<p><strong>TITRE III</strong></p>
<p><strong>DES CONSEILLERS TECHNIQUES</strong></p>
<p> </p>
<p><strong>Article 4</strong> : Les Conseillers Techniques effectuent toutes missions qui leur sont confiées par le Ministre ou le Secrétaire d'Etat.</p>
<p> </p>
<p><strong>TITRE IV</strong></p>
<p><strong>DE L'INSPECTION GENERALE</strong></p>
<p> </p>
<p><strong>Article 5</strong> :</p>
<p>(1) L'Inspection Générale comprend trois (03) Inspecteurs Généraux:</p>
<p>-          l'Inspecteur Général des Services;</p>
<p>-          l'Inspecteur Général du Développement Agricole;</p>
<p>-          l'Inspecteur Général du Développement Rural.</p>
<p>(2) La coordination des activités de l'Inspection Générale est assurée par l'Inspecteur Général le plus ancien dans le grade le plus élevé.</p>
<p>(3) L'Inspection Générale est chargée :</p>
<p><strong>a) en ce qui concerne les services de l'évaluation des performances des services par rapport aux objectifs fixés, en liaison avec le Secrétaire Général :</strong></p>
<p>-          du contrôle interne et de l'évaluation du fonctionnement des services centraux et déconcentrés, des établissements sous tutelle ainsi que des organismes et projets rattachés;</p>
<p>-          de l'information du Ministre sur la qualité du fonctionnement et du rendement des services;</p>
<p>-          de l'évaluation de l'application des techniques d'organisation et méthodes ainsi que de la simplification du travail administratif, en liaison avec les services compétents de la réforme administrative;</p>
<p>-          de la mise en œuvre de la stratégie de lutte contre la corruption au sein du Ministère, en liaison avec la cellule ministérielle de lutte contre la corruption.</p>
<p><strong>b) en ce qui concerne le développement agricole :</strong></p>
<p>-          du contrôle et de l'évaluation des investissements publics dans le cadre de la mise en œuvre de la politique agricole;</p>
<p>-          de l'évaluation des enquêtes et des statistiques agricoles;</p>
<p>-          du contrôle des organismes placés sous tutelle du Ministère, et des entreprises privées agricoles;</p>
<p>-          de l'évaluation des stratégies et des modalités de garantie de la sécurisation de l'autosuffisance alimentaire;</p>
<p>-          du contrôle de la conformité aux normes en matière des intrants agricoles;</p>
<p>-          de l'exploitation des rapports d'audit technique des programmes et projets de développement et de promotion agricoles;</p>
<p>-          du contrôle de la disponibilité et de l'accessibilité des intrants agricoles auprès des agriculteurs;</p>
<p>-          du contrôle et de l'évaluation des opérations agricoles;</p>
<p>-          de l'évaluation séquentielle des performances sectorielles en matière de développement agricole;</p>
<p>-          de l'inspection pédagogique des établissements en ce qui concerne le développement agricole.</p>
<p><strong>c) en ce qui concerne le développement rural :</strong></p>
<p>-          du contrôle et de l'évaluation des programmes d'amélioration du cadre de vie en milieu rural;</p>
<p>-          du contrôle et de l'évaluation du développement communautaire et des organismes professionnels;</p>
<p>-          du contrôle de l'aménagement de l'espace rural et du génie rural;</p>
<p>-          du contrôle du registre des sociétés coopératives et des groupes d'initiative commune;</p>
<p>-          du contrôle des projets et programmes;</p>
<p>-          du contrôle et de l'évaluation permanente des programmes agricoles, des enseignants et des différentes disciplines de l'enseignement agricole en ce qui concerne le développement rural;</p>
<p>-          de l'inspection pédagogique des établissements en ce qui concerne le développement rural;</p>
<p>-          de l'évaluation de la formation et de la coopération en ce qui concerne le développement rural;</p>
<p>-          de l'évaluation des postes et des unités techniques opérationnelles du génie rural et du développement local et communautaire;</p>
<p>-          du contrôle des performances des projets et programmes mis en œuvre dans le cadre de l'amélioration du cadre de vie en milieu rural, de l'aménagement de l'espace rural, du génie rural, du développement local et communautaire.</p>
<p>(3) Chaque Inspecteur Général est assisté de deux (02) Inspecteurs.</p>
<p> </p>
<p><strong>Article 6</strong> :</p>
<p>(1) Dans l'accomplissement de leurs missions, les Inspecteurs Généraux et les Inspecteurs ont accès à tous les documents des services contrôlés.</p>
<p>A ce titre, ils peuvent:</p>
<p>-          demander par écrit des informations, explications ou documents aux responsables des services contrôlés qui sont tenus de répondre dans les délais impartis par l'Inspecteur Général ou les Inspecteurs;</p>
<p>-          sur leur demande, et à titre ponctuel, disposer du personnel nécessaire relevant d'autres services du Ministère;</p>
<p>-          requérir la force publique en cas de nécessité, après avis du Ministre et conformément à la loi, en vue de leur prêter main forte ou constater par écrit les atteintes à la fortune publique.</p>
<p>(2) Chaque mission d'inspection ou de contrôle donne lieu à la rédaction d'un rapport adressé au Ministre, avec copie au Secrétaire Général.</p>
<p>Le Ministre transmet copie du rapport au Ministre chargé de la réforme administrative et au Ministre chargé du contrôle supérieur de l'état.</p>
<p>(3) Le Ministre adresse trimestriellement un rapport de contrôle ainsi que le rapport annuel d'activités de l'Inspection Générale au Premier Ministre, Chef du Gouvernement.</p>
<p> </p>
