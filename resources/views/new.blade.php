<p>-          de l'élaboration des indicateurs de performance sectoriels.</p>
<p>(2) Elle comprend:</p>
<p>-          la Cellule des Programmes d'Enseignement et de Formation;</p>
<p>-          la Cellule du Suivi et du Contrôle des Etablissements de Formation Agricole, Coopérative et Communautaire.</p>
<p> </p>
<p><strong>SECTION I</strong></p>
<p><strong>DE LA CELLULE DES PROGRAMMES D'ENSEIGNEMENT ET DE FORMATION</strong></p>
<p> </p>
<p><strong>Article 124</strong> :</p>
<p>(1) Placée sous l'autorité d'un Chef de Cellule, la Cellule des Programmes d'Enseignement et de Formation est chargée:</p>
<p>-          de la conception, de l'élaboration et de la mise en œuvre de la poli­tique de l'enseignement et de la formation agricoles et coopératifs;</p>
<p>-          de l'intégration des enseignements agricole, coopératif et communautaire dans les programmes d'enseignement scolaire et universitaire, en liaison avec les administrations concernées;</p>
<p>-          de la conception et de l'élaboration des programmes de formation initiale et/ou modulaire, continue et itinérante;</p>
<p>-          de l'organisation des examens et concours d'entrée dans les établissements publics d'enseignement agricole, en liaison avec les services et organismes concernés.</p>
<p>(2) Elle comprend, outre le Chef de Cellule, trois (03) Chargés d'Etudes Assistants.</p>
<p> </p>
<p><strong>SECTION Il</strong></p>
<p><strong>DE LA CELLULE DU SUIVI ET DU CONTROLE DES ETABLISSEMENTS</strong></p>
<p><strong>DE FORMATION AGRICOLE, COOPERATIVE ET COMMUNAUTAIRE</strong></p>
<p> </p>
<p><strong>Article 125</strong> :</p>
<p>(1) Placée sous l'autorité d'un Chef de Cellule, la Cellule du Suivi et du Contrôle des Etablissements de Formation Agricole, Coopérative et Communautaire est chargée:</p>
<p>-          du suivi du fonctionnement des établissements d'enseignement publics placés sous l'autorité ou la tutelle du Ministère;</p>
<p>-          du contrôle technique et pédagogique des établissements d'enseignement agricole, coopérative et communautaire privés.</p>
<p>(2) Elle comprend, outre le Chef de Cellule, trois (03) Chargés d'Etudes Assistants.</p>
<p> </p>
