<p>- les Délégations Départementales de l'Agriculture et du Développement Rural;</p>
<p>- les Délégations d'Arrondissement de l'Agriculture et du Développement Rural;</p>
<p>- les Délégations de District de l'Agriculture et du Développement Rural;</p>
<p> </p>
<p><strong>CHAPITRE I</strong></p>
<p><strong>DE LA DELEGATION PROVINCIALE DE L'AGRICULTURE</strong></p>
<p><strong>ET DU DEVELOPPEMENT RURAL</strong></p>
<p> </p>
<p><strong>Article 127</strong> :</p>
<p>(1) Placée sous l'autorité d'un Délégué, la Délégation Provinciale de l'Agriculture et du Développement Rural est chargée de :</p>
<p>-          l'animation, de la coordination, du contrôle et de l'évaluation à l'échelle de la Province des activités de l'ensemble des services du Ministère;</p>
<p>-          la gestion de l'ensemble des crédits délégués aux services</p>
<p>-          provinciaux;</p>
<p>-          la gestion de l'ensemble du personnel de la délégation;</p>
<p>-          la promotion de l'agriculture et du développement rural dans la Province;</p>
<p>-          l'animation et de l'évaluation des services d'appui technique.</p>
<p>(2) Dans l'accomplissement de ses missions, le Délégué Provincial est assisté de deux (02) Sous-Délégués chargés respectivement :</p>
<p>-          de la promotion de l'agriculture et du développement rural;</p>
<p>-          des services d'appui technique.</p>
<p>(3) La Délégation Provinciale comprend :</p>
<p>-          le Service du Développement de l'Agriculture;</p>
<p>-          le Service du Génie Rural et de l'Amélioration du Cadre de Vie en Milieu Rural ;</p>
<p>-          le Service du Développement local et Communautaire;</p>
<p>-          le Service du Registre des Sociétés Coopératives et des Groupes d'Initiative Communes;</p>
<p>-          l'Unité d'Appui aux Projets et aux Investisseurs;</p>
<p>-          la Base Phytosanitaire;</p>
<p>-          le Service des Enquêtes et Statistiques Agricoles;</p>
<p>-          le Service du Contrôle de Qualité des Intrants et Produits Agricoles;</p>
<p>-          le Service des Affaires Administratives et Financières.</p>
<p>(4) Le Sous-Délégué chargé de la Promotion de l'Agriculture et du Développement Rural, coordonne les services techniques ci-après:</p>
<p>-          le Service du Développement de l'Agriculture;</p>
<p>-          le Service du Génie Rural et de l'Amélioration du Cadre de Vie en Milieu Rural;</p>
<p>-          le Service du Développement Local et Communautaire;</p>
<p>-          la Base Phytosanitaire.</p>
<p>(5) Le Sous-Délégué chargé des Services d'Appui Techniques, coordonne les services techniques ci-après:</p>
<p>-          le Service du Registre des Sociétés Coopératives et des Groupes d'Initiative Communes;</p>
<p>-          l'Unité d'Appui aux Projets et aux Investisseurs;</p>
<p>-          le Service des Enquêtes et Statistiques Agricoles;</p>
<p>-          le Service du Contrôle de Qualité des Intrants et Produits Agricoles.</p>
<p> </p>
<p><strong>Article 128</strong> :</p>
<p>(1) Placé sous l'autorité d'un Chef de Service, le Service du Développement de l'Agriculture est chargé :</p>
<p>-          du suivi de la mise en œuvre des directives nationales en matière de développement agricole;</p>
<p>-          du suivi des programmes de production agricole;</p>
<p>-          de l'élaboration du calendrier agricole;</p>
<p>-          de l'encadrement des producteurs et de la vulgarisation agricole;</p>
<p>-          de la diffusion de l'information et des conseils aux producteurs;</p>
<p>-          de l'appui et du suivi des organisations professionnelles agricoles;</p>
<p>-          du développement des cadres de concertation entre les entrepreneurs agricoles et les partenaires du développement;</p>
<p>-          de la promotion de l'organisation des producteurs à la base;</p>
<p>-          de l'appui à l'émergence et au développement des organisations professionnelles et interprofessionnelles;</p>
<p>-          de la promotion de l'utilisation des semences et plants, des engrais et des moyens de lutte contre les ennemis des cultures;</p>
<p>-          de l'appui au développement des exploitations agricoles;</p>
<p>-          de l'appui aux producteurs en matière de mécanisation agricole.</p>
<p>(2) Il comprend:</p>
<p>-          le Bureau de Développement des Cultures;</p>
<p>-          le Bureau des Exploitations Agricoles;</p>
<p>-          le Bureau de la Vulgarisation Agricole;</p>
<p>-          le Bureau des Organisations Professionnelles Agricoles et de l'Action</p>
<p>-          Coopérative;</p>
<p>-          le Bureau des Semences et Plants.</p>
<p> </p>
<p><strong>Article 129</strong> :</p>
<p>(1) Placé sous l'autorité d'un Chef de Service, le Service du Génie Rural et de l'Amélioration du Cadre de Vie en Milieu Rural est chargé :</p>
<p>-          du suivi de la réalisation des programmes d'amélioration du cadre de vie en milieu rural;</p>
<p>-          de l'appui aux communautés rurales et aux collectivités territoriales décentralisées dans leurs projets d'équipements ruraux et d'amélioration du cadre de vie;</p>
<p>-          du suivi de la réalisation des programmes et projets d'aménagement et de gestion de l'espace rural;</p>
<p>-          de la mise en œuvre des politiques de développement de l'hydraulique agricole;</p>
<p>-          du suivi de la mise en œuvre des programmes et projets d'aménagement hydro-agricoles ;</p>
<p>-          du suivi de la mise en œuvre des programmes de gestion des ouvrages hydro-agricoles, en liaison avec les administrations et organismes concernés;</p>
<p>-          de l'appui aux producteurs et aux communautés rurales pour l'élaboration et la gestion de leurs projets hydro-agricoles ;</p>
<p>-          du suivi de la mise en œuvre de la politique de mécanisation agricole;</p>
<p>-          de la promotion des techniques et équipements de conservation et de transformation des produits agricoles et alimentaires, en liaison avec les services concernés;</p>
<p>-          de l'homologation des machines agricoles.</p>
<p>(2) Il comprend:</p>
<p>-          le Bureau des Infrastructures Rurales;</p>
<p>-          le Bureau de la Mécanisation Agricole;</p>
<p>-          le Bureau de l'Hydraulique Agricole;</p>
<p>-          le Bureau de l'Aménagement de l'Espace Rural;</p>
<p>-          le Bureau du Suivi de l'Amélioration du Cadre de Vie en Milieu Rural.</p>
<p> </p>
<p><strong>Article 130</strong> :</p>
<p>(1) Placé sous l'autorité d'un Chef de Service, le Service du Développement Local et Communautaire est chargé:</p>
<p>-          de la promotion de la participation effective des communautés rurales au processus de développement local.</p>
<p>-          de contribuer à l'émergence d'initiatives locales de développement ;</p>
<p>-          de l'appui à l'élaboration et à la mise en œuvre des plans de développement communaux et communautaires;</p>
<p>-          du suivi de la réalisation des programmes de développement communautaire, communal et participatif;</p>
<p>-          de la promotion de la politique de participation des communautés rurales à la réalisation des projets et à la maintenance des équipements ruraux d'intérêt collectif;</p>
<p>-          de la liaison avec les organisations non gouvernementales intervenant en milieu rural pour l'autopromotion des communautés rurales et du suivi de leurs programmes et activités;</p>
<p>-          de la promotion économique de la femme rurale et de la prise en compte de l'aspect genre dans les programmes de développement agricole et rural;</p>
<p>-          de la conception et du suivi des programmes d'éducation des adultes en milieu rural;</p>
<p>-          de la préparation et du suivi des programmes de formation dans les Centres d'Education et d'Action Communautaire.</p>
<p>(2) Il comprend:</p>
<p>-          le Bureau de l'Animation et des Actions d'Auto-Promotion;</p>
<p>-          le Bureau des Actions Agricoles Féminines.</p>
<p> </p>
<p><strong>Article 131</strong> :</p>
<p>(1) Le Service du Registre des Sociétés Coopératives et des Groupes d'Initiative Commune est chargé:</p>
<p>-          de la tenue du registre des sociétés coopératives et de leurs unions;</p>
<p>-          de l'étude des requêtes relatives à l'application des dispositions législatives et réglementaires régissant les sociétés et les groupes d'initiative commune;</p>
<p>-          de l'inscription des sociétés coopératives, des groupes d'initiative commune et de leurs unions respectives dans le registre prévu à cet effet;</p>
<p>-          de l'annulation de l'inscription sur le registre, en cas de fusion, scission ou dissolution;</p>
<p>-          de l'établissement et de la signature des certificats d'inscription;</p>
<p>-          de l'exécution du transfert du siège social des organisations à la demande de celles-ci;</p>
<p>-          de la mise à disposition des usagers des informations sur la procédure d'inscription et des dispositions législatives et réglementaires, en matière de coopératives et de groupes d'initiative commune</p>
<p>-          de la conservation des documents déposés par ces organisations et de leur mise à la disposition du public pour consultation;</p>
<p>-          de la transmission des copies de ces documents au Ministre ;</p>
<p>-          de la transmission au Ministre, dans un délai de trente (30) jours, du double de tout dossier abouti concernant l'inscription, la modification des statuts, le transfert du siège ou d'adresse, ou tout autre acte légal dont la publication est obligatoire;</p>
<p>-          de l'établissement d'un rapport annuel à transmettre au Ministre;</p>
<p>-          du suivi de l'application des lois et règlements propres aux sociétés coopératives et aux groupes d'initiative commune.</p>
<p>(2) Il comprend:</p>
<p>-          le Bureau du Courrier;</p>
<p>-          le Bureau de la Réglementation et des Actes.</p>
<p> </p>
<p><strong>Article 132</strong> :</p>
<p>(1) Placée sous l'autorité d'un Chef d'Unité, l'Unité d'Appui aux Projets et aux Investisseurs est chargée, en liaison avec les services techniques de la délégation et les administrations ou organismes concernés, de :</p>
<p>-          la définition et du suivi de la mise en oeuvre des solutions adaptées aux problèmes du développement agricole et rural dans le ressort de la Délégation Provinciale;</p>
<p>-          la participation à la planification des programmes d'amélioration du cadre de vie en milieu rural;</p>
<p>-          la formulation des projets de développement agricole et rural et de l'assistance à la recherche des financements ;</p>
<p>-          l'appui technique aux investisseurs et opérateurs privés agricoles.</p>
<p>(2) Elle comprend, outre le Chef d'Unité, cinq (05) Ingénieurs d'Etudes.</p>
<p> </p>
<p><strong>Article 133</strong> :</p>
<p>(1) Placée sous l'autorité d'un Chef de Base, la Base Phytosanitaire est chargée:</p>
<p>-          de la promotion de l'émergence des brigades phytosanitaires villageoises;</p>
<p>-          de l'appui aux brigades phytosanitaires villageoises;</p>
<p>-          de la mise en œuvre de la lutte contre les grands fléaux;</p>
<p>-          de la surveillance et de l'alerte phytosanitaires;</p>
<p>-          du contrôle phytosanitaire des produits agricoles et des intrants.</p>
<p>(2) Il comprend:</p>
<p>-          le Bureau de l'Encadrement et de la Formation Phytosanitaires;</p>
<p>-          le Bureau des Interventions Phytosanitaires.</p>
<p> </p>
<p><strong>Article 134</strong> :</p>
<p>(1) Placé sous l'autorité d'un Chef de Service, le Service des Enquêtes Statistiques Agricoles est chargé:</p>
<p>-          du suivi de la mise en œuvre de la politique en matière de collecte et de production des statistiques agricoles;</p>
<p>-          du suivi des comptes et des revenus de l'agriculture;</p>
<p>-          de la collecte des statistiques agricoles;</p>
<p>-          de la mise en œuvre d'un réseau d'information sur le revenu des exploitants agricoles;</p>
<p>-          de la gestion et du suivi du matériel d'enquête dans la Province;</p>
<p>-          du suivi des statistiques sur les productions, les exploitations et les marchés agricoles.</p>
<p>(2) Il comprend:</p>
<p>-          le Bureau des Enquêtes Agronomiques et Agro-économiques ;</p>
<p>-          le Bureau des Analyses et de Prévisions Agricoles;</p>
<p>-          le Bureau de Statistiques Agricoles.</p>
<p> </p>
<p><strong>Article 135</strong> :</p>
<p>(1) Placé sous l'autorité d'un Chef de Service, le Service du Contrôle et de Qualité des Intrants et Produits Agricoles est chargé:</p>
<p>-          du suivi de la certification des semences et plants;</p>
<p>-          du suivi du contrôle de qualité des intrants et produits agricoles;</p>
<p>-          du suivi du contrôle phytosanitaire aux frontières;</p>
<p>-          de la participation à l'homologation des pesticides à usage agricole;</p>
<p>-          de la participation à l'homologation des espèces et variétés, en liaison avec le Conseil National des Semences et Obtentions Végétales;</p>
<p>-          du suivi des normes de contrôle de la qualité des engrais;</p>
<p>-          du suivi de la réglementation des biotechnologies pour l'alimentation et l'agriculture et du contrôle de sa mise en application;</p>
<p>-          du suivi de la mise en œuvre de la politique de certification de l'origine des produits agricoles;</p>
<p>-          de l'inspection des produits.</p>
<p>(2)  Il comprend :</p>
<p>-          le Bureau de Contrôle et de la Certification des Semences et Plants;</p>
<p>-          le Bureau d'Inspection Phytosanitaire;</p>
<p>-          le Bureau de Poste de Police Phytosanitaire;</p>
<p>-          le Bureau de Contrôle des Engrais;</p>
<p>-          le Bureau de Contrôle des Pesticides.</p>
<p> </p>
<p><strong>Article 136</strong> :</p>
<p>(1) Placé sous l'autorité d'un Chef de Service, le Service des Affaires Administratives et Financières est chargé de :</p>
<p>-          la gestion du personnel;</p>
<p>-          la préparation et de l'exécution du budget;</p>
<p>-          la commande et de la maintenance du matériel;</p>
<p>-          l'entretien des bâtiments;</p>
<p>-          la collecte, de la centralisation et de la diffusion des documents;</p>
<p>-          la conservation des archives.</p>
<p>(2) Il comprend:</p>
<p>-          le Bureau du Courrier;</p>
<p>-          le Bureau du Personnel;</p>
<p>-          le Bureau du Budget et du Matériel;</p>
<p>-          le Bureau de la Documentation et des Archives.</p>
